package typeCasting;

public class TypeCasting {
	
	public static void main(String[] args) {
		// Type conversions / type casting
		
		//int i = 10;
		//long j = i;
		
		//Widening primitive conversion.
		
		long j = 467323232;
		
		int i = (int)j; //Narrowing primitive conversion.
		
		System.out.println(i);
		
		//int k = 5;
		float f = 5.5f;
		int k = (int)f;
		System.out.println(k);
		

	}

}
